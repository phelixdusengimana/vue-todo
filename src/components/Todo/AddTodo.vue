<template>
    <div class="add__todo">
       <form @submit="addTodo">
           <input type="text" name="title" v-model="title"/>
           <button type="submit">Add &#x2b;</button>
       </form>
    </div>
</template>

<script>
import  {v4 as uuidv4} from 'uuid';

export default {
    name: "AddTodo",
    data() {
        return{
            title: ''
        }
    },
    methods:{
        addTodo(e){
            e.preventDefault();
            const newTodo = {
                id: uuidv4,
                title: this.title,
                completed: false
            }
           this.$emit("add-todo",newTodo);
           this.title = ''
        }
    }
}
</script>
